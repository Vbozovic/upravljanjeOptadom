package klasifikacija.incomingEntities;

public class KarakterCheckRequest {


	private String indeksA;
	private String indeksProces;
	private String indeksDeo;
	
	
	
	public KarakterCheckRequest() {
		super();
	}
	public String getIndeksA() {
		return indeksA;
	}
	public void setIndeksA(String indeksA) {
		this.indeksA = indeksA;
	}
	public String getIndeksProces() {
		return indeksProces;
	}
	public void setIndeksProces(String indeksProces) {
		this.indeksProces = indeksProces;
	}
	public String getIndeksDeo() {
		return indeksDeo;
	}
	public void setIndeksDeo(String indeksDeo) {
		this.indeksDeo = indeksDeo;
	}
	
	
	
	
}
